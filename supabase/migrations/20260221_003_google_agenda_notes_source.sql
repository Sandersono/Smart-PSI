  id bigint generated by default as identity primary key,
  clinic_id uuid not null references public.clinics (id) on delete cascade,
  user_id uuid not null references auth.users (id) on delete cascade,
  calendar_id text not null default 'primary',
  channel_id text not null unique,
  resource_id text not null,
  expiration_at timestamptz,
  active boolean not null default true,
  created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.calendar_blocks (
  id bigint generated by default as identity primary key,
  clinic_id uuid not null references public.clinics (id) on delete cascade,
  provider_user_id uuid not null references auth.users (id) on delete cascade,
  title text not null,
  start_time timestamptz not null,
  end_time timestamptz not null,
  google_event_id text,
  source text not null default 'manual',
  created_at timestamptz not null default timezone('utc', now())
);

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'ck_calendar_blocks_time'
      and conrelid = 'public.calendar_blocks'::regclass
  ) then
    alter table public.calendar_blocks
      add constraint ck_calendar_blocks_time
      check (end_time > start_time);
  end if;

  if not exists (
    select 1
    from pg_constraint
    where conname = 'ck_calendar_blocks_source'
      and conrelid = 'public.calendar_blocks'::regclass
  ) then
    alter table public.calendar_blocks
      add constraint ck_calendar_blocks_source
      check (source in ('manual', 'google_external'));
  end if;
end $$;

