alter table public.appointments
  add column if not exists provider_user_id uuid references auth.users (id) on delete cascade;

update public.appointments
set provider_user_id = coalesce(provider_user_id, user_id)
where provider_user_id is null;

alter table public.appointments
  alter column provider_user_id set not null;

alter table public.appointments
  add column if not exists is_block boolean not null default false,
  add column if not exists google_event_id text,
  add column if not exists google_calendar_id text,
  add column if not exists google_sync_status text default 'pending',
  add column if not exists google_last_synced_at timestamptz;

update public.appointments
set google_sync_status = coalesce(google_sync_status, 'pending')
where google_sync_status is null;

alter table public.notes
  add column if not exists source text;

update public.notes
set source = 'manual'
where source is null;

alter table public.notes
  alter column source set not null,
  alter column source set default 'manual';

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'ck_notes_source'
      and conrelid = 'public.notes'::regclass
  ) then
    alter table public.notes
      add constraint ck_notes_source
      check (source in ('audio', 'quick', 'manual'));
  end if;
end $$;

